package bean;

public class Date {
	
	private String name = null;
	private String time = null;
    private int age = 0;
   
    public String getName() {
           return name;
    }
    public void setName(String name) {
           this.name = name;
    }
    public int getAge() {
           return age;
    }
    public void setAge(int age) {
           this.age = age;
    }
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
}
